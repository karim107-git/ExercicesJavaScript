//fonction qui additionne eux nombres
function somme(x,y){
    return x+y;
}
//affichage du resultat
console.log(somme(100,50));  //Affiche  150
console.log(somme(-10,10));  //Affiche 0
console.log(somme(2,3));     //Affiche 5
console.log(somme(5.2,-7.5)); //Affiche -2.3
console.log(somme(1,70)); //Affiche 71
console.log(somme(0,0));  //Affiche 0







